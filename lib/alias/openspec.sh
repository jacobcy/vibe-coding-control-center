#!/usr/bin/env zsh
# OpenSpec aliases

# @desc OpenSpec CLI proxy
alias os='openspec'
# @desc Initialize OpenSpec in current repo
alias osi='openspec init'
# @desc List current OpenSpec changes
# @featured
alias osl='openspec list'
# @desc View a specific OpenSpec change
alias osv='openspec view'
# @desc Start a new OpenSpec change
# @featured
alias osn='openspec new'
# @desc Validate OpenSpec files
alias osval='openspec validate'
